                    <h2>About FIRST and FRC</h2>

                    <p>FIRST (For Inspiration and Recognition of Science and Technology), is an organization dedicated to promote Science and technology to future achievers. FIRST offers a variety of programs to have young adults solve real world problems with even Lego products. FIRST also operates all around the world, not just in the United States. FIRST helps develop young minds throughout most of the world.</p>
                    
                    <p>Technology and science is not the only things that FIRST emphasizes. FIRST gives young adults key values, like teamwork, critical thinking, and leadership. FIRST strongly promotes quality work, Respect to others, and the worth of individuals, otherwise known as gracious professionalism. In the words of FIRST itself, &quot;Gracious professionals learn and compete like crazy, but treat one another with respect and kindness in the process.&quot; After graduation and being part of FIRST offers the opportunity of over 14 million dollars worth of scholarships in 138 institutions.</p>
                    
                    <p><strong>Jr. FLL</strong>, the <strong>Junior FIRST Lego League</strong>, is FIRST's program for children grades K-3 (ages 6-9). It is meant to give children in early interest interest in mechanics, science, and technology, and encourage them to move on to higher-level programs like the FIRST Lego League. </p>
                    
                    <p><strong>FLL</strong>, the <strong>FIRST Lego League</strong>, is the next step up. It's for kids in grades 4-8 (ages 9-14). It was designed to bring such younger students into the world of engineering by building Lego-based robots to overcome real engineering challenges.</p>
                    
                    <p>The <strong>FIRST Tech Challenge</strong> (<strong>FTC</strong>) is one of FIRST's programs for high-school students in grades 9-12 (aged 15-18). Teams of up to 10 students will design, build, program, and compete their robots with and against other FTC teams.</p>
                    
                    <p>The Pittsford Panthers are involved with FIRST's other high school program called "<strong>FRC</strong>." FRC stands for <strong>FIRST Robotics Competition</strong>, a worldwide competition featuring over 3000 teams competing with robots that the students build in their teams to solve problems FIRST gives them, also known as the game. The game, along with a ceremony, is shown nationwide/worldwide to every competing team at many local kickoffs at the start of the New Year. After the game is shown (this year&#39;	s, Logomotion), teams have six weeks to build robot(s) to solve the game. At the end of six weeks, robots are sent to storage and stay until the regional/championship competition(s) in the area of the team.</p>
                    
                    <p>In a nutshell, FIRST is one of the most influential organizations in the world. They provide employees and employers who could make changes to the world, even if the profession isn't robotics. As said in the 2011 kickoff "FIRST cures cancer, FIRST provides jobs..." the ways are endless of what FIRST did to average people.</p>


